function AC_AddExtension(src, ext)
{
  if (src.indexOf('?') != -1)
    return src.replace(/\?/, ext+'?'); 
  else
    return src + ext;
}



//generuje html
function AC_Generateobj(objAttrs, params, embedAttrs, intoElem) 
{ 
	obj = document.createElement('embed');
	for (var i in embedAttrs)
		obj.setAttribute(i, embedAttrs[i]);

	return obj;
}



